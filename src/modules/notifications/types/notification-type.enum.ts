/**
 * NotificationType Enum - Types of notifications in the system
 *
 * NEW_DEAL: Generated when a new deal is created/approved
 * PRICE_DROP: Generated when a deal's price decreases
 * PRICE_INCREASE: Generated when a deal's price increases
 */
export enum NotificationType {
  NEW_DEAL = 'NEW_DEAL',
  PRICE_DROP = 'PRICE_DROP',
  PRICE_INCREASE = 'PRICE_INCREASE',
}
